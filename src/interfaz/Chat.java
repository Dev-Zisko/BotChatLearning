    private void buLearnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buLearnActionPerformed
        try{
            control.addQuestions((txtQuestion.getText()).toLowerCase(), (txtAnswer.getText()).toLowerCase());
            JOptionPane.showMessageDialog(this, "El robot esta aprendiendo. Muchas Gracias!");
            txtQuestion.setText("");
            txtAnswer.setText("");
        } catch (Exception ex){
            System.out.println("Error al intentar aprender. "+ ex);
        }     
    }//GEN-LAST:event_buLearnActionPerformed

    private void buChatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buChatActionPerformed
        try{
            String historial = txtArea.getText();
            String answer = control.sendQuestion((txtChat.getText()).toLowerCase());
            if(historial.equals("")){
                txtArea.setText(answer);
                txtArea.append(System.getProperty("line.separator"));
            }
            else{
                txtArea.setText(historial+answer);
                txtArea.append(System.getProperty("line.separator"));
            }
            
        } catch (Exception ex){
            System.out.println("Error al enviar el mensaje. " + ex);
        }
    }//GEN-LAST:event_buChatActionPerformed

