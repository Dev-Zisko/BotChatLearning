    private void txtChatKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtChatKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try{
                String historial = txtArea.getText();
                String answer = control.sendQuestion((txtChat.getText()).toLowerCase());
                if(!answer.equals("")){
                    if(historial.equals("")){
                        txtArea.setText(answer);
                        txtArea.append(System.getProperty("line.separator"));
                    }
                    else{
                        txtArea.setText(historial+answer);
                        txtArea.append(System.getProperty("line.separator"));
                    }
                    txtChat.setText("");                    
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se que responder ense√±ame. Gracias!");
                    txtChat.setText("");
                }   
            } catch (Exception ex){
                System.out.println("Error al enviar el mensaje. " + ex);
            }            
        }
    }//GEN-LAST:event_txtChatKeyPressed

