    private void buEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buEnterActionPerformed
        User user = new User(txtUsername.getText(), txtPassword.getText());
        boolean login = control.login(user);
        if(login==true){
            JOptionPane.showMessageDialog(this, "Sesión iniciada Correctamente. Bienvenido!.");
            Chat chat =new Chat(txtUsername.getText(), txtPassword.getText());
            chat.setVisible(true);
            chat.setLocationRelativeTo(null);
            dispose();
        }
        else
            JOptionPane.showMessageDialog(this, "Datos Inválidos. Intente nuevamente.");
    }//GEN-LAST:event_buEnterActionPerformed

